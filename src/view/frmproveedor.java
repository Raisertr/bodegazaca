    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
         int filaSeleccionada = tblProveedores.getSelectedRow();
    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Seleccione un proveedor de la tabla");
        return;
    }

    int idProveedor = (int) tblProveedores.getValueAt(filaSeleccionada, 0);

    int confirm = JOptionPane.showConfirmDialog(this,
        "¿Está seguro de desactivar este proveedor?",
        "Confirmar desactivación",
        JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        proveedorController controller = new proveedorController();
        boolean exito = controller.desactivarProveedor(idProveedor);

        if (exito) {
            JOptionPane.showMessageDialog(this, "Proveedor desactivado correctamente");
            cargarProveedores();
        } else {
            JOptionPane.showMessageDialog(this, "Error al desactivar el proveedor");
        }
    }
    }//GEN-LAST:event_jButton2ActionPerformed

