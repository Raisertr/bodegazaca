    private void tblProveedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProveedoresMouseClicked
        // TODO add your handling code here:
        int filaSeleccionada = tblProveedores.getSelectedRow();
    if (filaSeleccionada != -1) {
        int idProveedor = (int) tblProveedores.getValueAt(filaSeleccionada, 0);

        proveedorController controller = new proveedorController();
        proveedor p = controller.buscarProveedorPorId(idProveedor);

        if (p != null) {
            txtRazonSocial.setText(p.getRazonSocial());
            txtTelefono.setText(p.getTelefono());
            txtCorreo.setText(p.getCorreo());
            txtDireccion.setText(p.getDireccion());
            txtRuc.setText(p.getRuc());
            txtContacto.setText(p.getContacto());
            txtCorreoContacto.setText(p.getCorreoContacto());
        }
    }
    }//GEN-LAST:event_tblProveedoresMouseClicked

     private void cargarProveedores() {
    DefaultTableModel modelo = (DefaultTableModel) tblProveedores.getModel();
    modelo.setRowCount(0);

    proveedorController controller = new proveedorController();
    List<proveedor> proveedores = controller.obtenerProveedoresActivos();

    for (proveedor p : proveedores) {
        Object[] fila = {
            p.getIdProveedor(),
            p.getRazonSocial(),
            p.getTelefono(),
            p.getCorreo(),
            p.getDireccion(),
            p.getRuc(),
            p.getContacto(),
            p.getCorreoContacto()
        };
        modelo.addRow(fila);
    }}

